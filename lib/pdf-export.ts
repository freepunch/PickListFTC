/**
 * PDF export helpers for PickListFTC.
 * Uses dynamic imports so jsPDF is never bundled for SSR.
 */

import type { TeamReport, TeamEventStats2025 } from "@/lib/types";
import type { ScoutNote } from "@/lib/notes";

// ── Local types ───────────────────────────────────────────────────────────────

export interface PickListEntry {
  teamNumber: number;
  teamName: string;
  opr: number;
  notes: string;
  picked: boolean;
}

// ── Color palette (RGB tuples) ────────────────────────────────────────────────

type RGB = [number, number, number];

const C = {
  accent:      [59,  130, 246] as RGB, // blue-500
  navy:        [15,  30,  70]  as RGB, // dark header for report
  black:       [17,  24,  39]  as RGB,
  darkGray:    [55,  65,  81]  as RGB,
  gray:        [107, 114, 128] as RGB,
  lightGray:   [229, 231, 235] as RGB,
  veryLight:   [249, 250, 251] as RGB,
  white:       [255, 255, 255] as RGB,
  takenGray:   [156, 163, 175] as RGB,
  greenBg:     [236, 253, 245] as RGB, // emerald-50
  amberBg:     [255, 251, 235] as RGB, // amber-50
  redBg:       [254, 242, 242] as RGB, // red-50
};

// ── Shared constants ──────────────────────────────────────────────────────────

const MARGIN   = 14;  // mm
const FOOTER_H = 16;  // mm reserved at bottom for footer

// ── Timestamp helpers ─────────────────────────────────────────────────────────

function tsFile(d: Date): string {
  const p = (n: number) => String(n).padStart(2, "0");
  return `${d.getFullYear()}${p(d.getMonth() + 1)}${p(d.getDate())}_${p(d.getHours())}${p(d.getMinutes())}`;
}

function tsDisplay(d: Date): string {
  return d.toLocaleDateString("en-US", {
    month: "long", day: "numeric", year: "numeric",
  }) + " at " + d.toLocaleTimeString("en-US", { hour: "numeric", minute: "2-digit" });
}

// ── Footer drawer (call after all content is placed) ─────────────────────────

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function addFooters(doc: any, timestamp: string) {
  const totalPages = doc.getNumberOfPages();
  const pw = doc.internal.pageSize.getWidth();
  const ph = doc.internal.pageSize.getHeight();

  for (let i = 1; i <= totalPages; i++) {
    doc.setPage(i);

    // Separator line
    doc.setDrawColor(...C.lightGray);
    doc.setLineWidth(0.3);
    doc.line(MARGIN, ph - FOOTER_H + 2, pw - MARGIN, ph - FOOTER_H + 2);

    doc.setFontSize(7.5);
    doc.setFont("helvetica", "normal");
    doc.setTextColor(...C.gray);

    // Left: branding
    doc.text("Generated by PickListFTC — picklistftc.com", MARGIN, ph - FOOTER_H + 7);

    // Center: page number
    doc.text(`Page ${i} of ${totalPages}`, pw / 2, ph - FOOTER_H + 7, { align: "center" });

    // Right: attribution + timestamp
    doc.text(`Built by First Try #21364  ·  ${timestamp}`, pw - MARGIN, ph - FOOTER_H + 7, { align: "right" });
  }
}

// ── Section heading helper ────────────────────────────────────────────────────

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function sectionHeading(doc: any, text: string, x: number, y: number, contentWidth: number) {
  doc.setFontSize(8);
  doc.setFont("helvetica", "bold");
  doc.setTextColor(...C.gray);
  doc.text(text.toUpperCase(), x, y);
  doc.setDrawColor(...C.lightGray);
  doc.setLineWidth(0.3);
  doc.line(x + doc.getTextWidth(text.toUpperCase()) + 3, y - 0.5, x + contentWidth, y - 0.5);
}

// ── Page overflow helper ──────────────────────────────────────────────────────

// eslint-disable-next-line @typescript-eslint/no-explicit-any
function ensureSpace(doc: any, y: number, needed: number, margin = MARGIN): number {
  const ph = doc.internal.pageSize.getHeight();
  if (y + needed > ph - FOOTER_H - 4) {
    doc.addPage();
    return margin + 4;
  }
  return y;
}

