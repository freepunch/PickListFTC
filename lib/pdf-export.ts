// Pick List PDF
// ─────────────────────────────────────────────────────────────────────────────

export async function exportPickListPDF(
  eventName: string,
  eventCode: string,
  entries: PickListEntry[]
) {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const jsPDF = (await import("jspdf") as any).default;
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  const autoTable = (await import("jspdf-autotable") as any).default;

  const now = new Date();
  const doc = new jsPDF({ orientation: "portrait", unit: "mm", format: "letter" });
  const pw = doc.internal.pageSize.getWidth();

  const active = entries.filter((e) => !e.picked);
  const taken  = entries.filter((e) => e.picked);

  // ── Header bar ──
  const BAR_H = 20;
  doc.setFillColor(...C.accent);
  doc.rect(0, 0, pw, BAR_H, "F");

  doc.setFont("helvetica", "bold");
  doc.setFontSize(14);
  doc.setTextColor(...C.white);
  doc.text("PickListFTC", MARGIN, 13);

  // Filename-safe date on right
  doc.setFontSize(8);
  doc.setTextColor(200, 220, 255);
  doc.text(tsDisplay(now), pw - MARGIN, 13, { align: "right" });

  // ── Event info block ──
  let y = BAR_H + 8;

  doc.setFont("helvetica", "bold");
  doc.setFontSize(12);
  doc.setTextColor(...C.black);
  doc.text(eventName, MARGIN, y);
  y += 5.5;

  doc.setFont("helvetica", "normal");
  doc.setFontSize(9);
  doc.setTextColor(...C.gray);
  doc.text(`Event Code: ${eventCode.toUpperCase()}`, MARGIN, y);
  doc.text(`${entries.length} team${entries.length !== 1 ? "s" : ""} ranked  ·  ${active.length} active, ${taken.length} taken`, pw - MARGIN, y, { align: "right" });
  y += 7;

  // ── Active pick list table ──
  const takenActiveSet = new Set<number>(); // row indices within the ACTIVE table that are taken — N/A here

  autoTable(doc, {
    startY: y,
    head: [["#", "Team", "Name", "OPR", "Notes"]],
    body: active.map((e, i) => [
      `${i + 1}`,
      String(e.teamNumber),
      e.teamName,
      e.opr.toFixed(1),
      e.notes,
    ]),
    styles: {
      fontSize: 9,
      cellPadding: { top: 2.5, bottom: 2.5, left: 3, right: 3 },
      textColor: C.black,
      lineColor: C.lightGray,
      lineWidth: 0.2,
    },
    headStyles: {
      fillColor: C.accent,
      textColor: C.white,
      fontStyle: "bold",
      fontSize: 8,
    },
    alternateRowStyles: {
      fillColor: C.veryLight,
    },
    columnStyles: {
      0: { cellWidth: 12, halign: "center", textColor: C.gray },
      1: { cellWidth: 20, fontStyle: "bold", halign: "center" },
      2: { cellWidth: "auto" },
      3: { cellWidth: 22, halign: "right", font: "courier" },
      4: { cellWidth: 50, textColor: C.darkGray },
    },
    margin: { left: MARGIN, right: MARGIN, bottom: FOOTER_H + 2 },
  });

  // ── Taken section (if any) ──
  if (taken.length > 0) {
    // eslint-disable-next-line @typescript-eslint/no-explicit-any
    y = (doc as any).lastAutoTable.finalY + 10;
    y = ensureSpace(doc, y, 30);

    sectionHeading(doc, "Taken by Other Alliances", MARGIN, y, pw - MARGIN * 2);
    y += 5;

    autoTable(doc, {
      startY: y,
      head: [["", "Team", "Name", "OPR", "Notes"]],
      body: taken.map((e) => [
        "—",
        String(e.teamNumber),
        e.teamName,
        e.opr.toFixed(1),
        e.notes,
      ]),
      styles: {
        fontSize: 9,
        cellPadding: { top: 2.5, bottom: 2.5, left: 3, right: 3 },
        textColor: C.takenGray,
        lineColor: C.lightGray,
        lineWidth: 0.2,
      },
      headStyles: {
        fillColor: C.lightGray,
        textColor: C.gray,
        fontStyle: "bold",
        fontSize: 8,
      },
      columnStyles: {
        0: { cellWidth: 12, halign: "center" },
        1: { cellWidth: 20, fontStyle: "bold", halign: "center" },
        2: { cellWidth: "auto" },
        3: { cellWidth: 22, halign: "right", font: "courier" },
        4: { cellWidth: 50 },
      },
      // Strikethrough line through each taken row
      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      didDrawCell: (data: any) => {
        if (data.section === "body") {
          const midY = data.cell.y + data.cell.height / 2;
          doc.setDrawColor(...C.takenGray);
          doc.setLineWidth(0.25);
          doc.line(data.cell.x + 1, midY, data.cell.x + data.cell.width - 1, midY);
        }
      },
      margin: { left: MARGIN, right: MARGIN, bottom: FOOTER_H + 2 },
    });
  }

  addFooters(doc, tsDisplay(now));
  doc.save(`${eventCode.toUpperCase()}_PickList_${tsFile(now)}.pdf`);
}

